string = '''
+------------------------------------------+
|................................^v........|
|...............................^..v.......|
|..............................^....v......|
|.............................^......v.....|
|............................^........v....|
|.......__..................^..........v...|
|......^..v................^............v..|
|.^v..^....v........^v....^..............v.|
|^..v^......v......^..v..^................v|
|............v....^....v^..................|
|.............v__^.........................|
+------------------------------------------+
'''

for line in string.split():
    inMountain = line.find('^') > line.find('v')
    for char in line:
        if char == '.':
            print('#' if inMountain else '.', end='')
        elif char == '^' or char == 'v':
            inMountain = char == '^'
            print('/' if char=='^' else '\\', end='')
        else:
            print(char, end='')
    print()

